let cachedEnv: Env | null = null;

export function validateEnv(): Env {
  if (cachedEnv) return cachedEnv;

  generateEnvExample();

  const result = envSchema.safeParse(process.env);

  if (!result.success) {
    const missingKeys = result.error.issues.map(
      (issue) => `${issue.path.join('.')}: ${issue.message}`
    );

    if (process.env.NODE_ENV === 'production') {
      console.error(new EnvironmentError(missingKeys).message);
      if (typeof process !== 'undefined' && process.exit) {
        process.exit(1);
      }
    } else {
      console.warn(
        `\n⚠️ [CreatorOS Env Warning] Missing or unconfigured environment variables:\n${missingKeys
          .map((k) => `  - ${k}`)
          .join('\n')}\n`
      );
    }
  }

  cachedEnv = (result.success ? result.data : process.env) as unknown as Env;
  return cachedEnv;
}

export const env = validateEnv();
