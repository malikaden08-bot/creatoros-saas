import { z } from 'zod';
import * as fs from 'fs';
import * as path from 'path';

export const envSchema = z.object({
  DATABASE_URL: z.string().min(1, 'DATABASE_URL is required (e.g. postgresql://user:pass@host:5432/db)'),
  REDIS_URL: z.string().min(1, 'REDIS_URL is required (e.g. redis://localhost:6379)'),

  NEXTAUTH_SECRET: z.string().min(16, 'NEXTAUTH_SECRET is required (min 16 characters)'),
  NEXTAUTH_URL: z.string().url().default('http://localhost:3000'),

  OPENAI_API_KEY: z.string().min(1, 'OPENAI_API_KEY is required'),
  GEMINI_API_KEY: z.string().min(1, 'GEMINI_API_KEY is required'),
  ANTHROPIC_API_KEY: z.string().min(1, 'ANTHROPIC_API_KEY is required'),
  GROQ_API_KEY: z.string().min(1, 'GROQ_API_KEY is required'),
  FAL_KEY: z.string().min(1, 'FAL_KEY is required'),
  REPLICATE_API_TOKEN: z.string().min(1, 'REPLICATE_API_TOKEN is required'),
  ELEVENLABS_API_KEY: z.string().min(1, 'ELEVENLABS_API_KEY is required'),
  DEEPGRAM_API_KEY: z.string().min(1, 'DEEPGRAM_API_KEY is required'),

  CLOUDINARY_CLOUD_NAME: z.string().min(1, 'CLOUDINARY_CLOUD_NAME is required'),
  CLOUDINARY_API_KEY: z.string().min(1, 'CLOUDINARY_API_KEY is required'),
  CLOUDINARY_API_SECRET: z.string().min(1, 'CLOUDINARY_API_SECRET is required'),

  NODE_ENV: z.enum(['development', 'test', 'production']).default('development'),
  PORT: z.string().default('3000')
});

export type Env = z.infer<typeof envSchema>;

export class EnvironmentError extends Error {
  public missingKeys: string[];
  constructor(missingKeys: string[]) {
    const formatted = missingKeys.map((k) => ` ❌  ${k}`).join('\n');
    const msg = `
================================================================================
🚨 CreatorOS ENVIRONMENT VALIDATION ERROR — APPLICATION STARTUP HALTED
================================================================================
The following required environment variables are missing or invalid:

${formatted}

Please check your .env.local file or server environment variables.
See .env.example for required keys and formats.
================================================================================
`;
    super(msg);
    this.name = 'EnvironmentError';
    this.missingKeys = missingKeys;
  }
}

